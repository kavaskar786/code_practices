import { motion } from "framer-motion";
import { useSpring, animated } from "react-spring";

// Animated Background Component
const AnimatedBackground = () => {
  const props = useSpring({
    from: { background: "linear-gradient(120deg, #f093fb 0%, #f5576c 100%)" },
    to: async (next) => {
      while (1) {
        await next({
          background: "linear-gradient(120deg, #5ee7df 0%, #b490ca 100%)",
        });
        await next({
          background: "linear-gradient(120deg, #f093fb 0%, #f5576c 100%)",
        });
      }
    },
    config: { duration: 5000 },
  });

  return <animated.div style={props} className="absolute inset-0 z-0" />;
};

// Parallax Card Component
const ParallaxCard = ({ children }) => {
  const calc = (x, y) => [
    -(y - window.innerHeight / 2) / 20,
    (x - window.innerWidth / 2) / 20,
    1.1,
  ];
  const trans = (x, y, s) =>
    `perspective(600px) rotateX(${x}deg) rotateY(${y}deg) scale(${s})`;

  const [props, set] = useSpring(() => ({
    xys: [0, 0, 1],
    config: { mass: 5, tension: 350, friction: 40 },
  }));

  return (
    <animated.div
      className="w-full max-w-md bg-white bg-opacity-90 rounded-lg shadow-2xl overflow-hidden"
      onMouseMove={({ clientX: x, clientY: y }) => set({ xys: calc(x, y) })}
      onMouseLeave={() => set({ xys: [0, 0, 1] })}
      style={{ transform: props.xys.interpolate(trans) }}
    >
      {children}
    </animated.div>
  );
};

